namespace LL.Graphics2D
{
    public struct RawRectangle
    {
        public int X;
        public int Y;
        public int Width;
        public int Height;

        public RawRectangle(int x, int y, int width, int height)
        {
            X = x;
            Y = y;
            Width = width;
            Height = height;
        }
    }
}
