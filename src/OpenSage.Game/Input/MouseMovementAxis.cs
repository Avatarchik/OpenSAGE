namespace OpenSage.Input
{
    /// <summary>
    /// Mouse movement axis, used to configure <see cref="MouseMovementInputKey"/>.
    /// </summary>
    public enum MouseMovementAxis
    {
        /// <summary>
        /// X axis.
        /// </summary>
        XAxis,

        /// <summary>
        /// Y axis.
        /// </summary>
        YAxis,

        /// <summary>
        /// Third axis, usually the mouse scroll wheel.
        /// </summary>
        ThirdAxis
    }
}