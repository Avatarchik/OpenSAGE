namespace OpenSage.Input
{
    /// <summary>
    /// System key, passed to <see cref="InputSystem.IsSystemKeyDown"/>.
    /// </summary>
    public enum SystemKey
    {
        /// <summary>
        /// Left or right Alt key.
        /// </summary>
        Alt,

        /// <summary>
        /// Left or right Control key.
        /// </summary>
        Control,

        /// <summary>
        /// Left or right Shift key.
        /// </summary>
        Shift
    }
}